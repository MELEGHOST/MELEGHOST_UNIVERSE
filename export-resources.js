const axios = require('axios');
const fs = require('fs');

const FIGMA_TOKEN = process.env.FIGMA_TOKEN;
const FIGMA_FILE_ID = process.env.FIGMA_FILE_ID;

const exportResources = async () => {
  try {
    console.log('Fetching data from Figma...');
    const response = await axios.get(`https://api.figma.com/v1/files/${FIGMA_FILE_ID}`, {
      headers: { 'X-Figma-Token': FIGMA_TOKEN },
    });

    console.log('Figma file structure:', JSON.stringify(response.data, null, 2));

    // Пример: Создание CSS файла
    fs.writeFileSync('styles.css', '/* Generated by Figma */\n');
    console.log('CSS file created.');

    // Здесь можно добавить логику для экспорта изображений или других ресурсов
  } catch (error) {
    console.error('Error exporting resources:', error.message);
    process.exit(1); // Завершаем выполнение с ошибкой
  }
};

exportResources();
